// ============================================================
// sprite_rom.v — Pixel Art Sprite Storage
// ============================================================
// Stores 16x16 pet sprites and 8x8 icon sprites.
//
// Address format:
//   addr[7:4] = sprite ID (0-15)
//   addr[3:0] = row within sprite (0-15)
//
// Data output: 16 bits = one row of pixels (MSB = leftmost)
//
// Sprite IDs:
//   0 = Happy face (frame 1 — standing)
//   1 = Happy face (frame 2 — bouncing up)
//   2 = Hungry face
//   3 = Sick face
//   4 = Sleeping face
//   5 = Dead face
//   6 = Poop (8x8, stored in lower 8 bits)
//   7 = Heart (8x8, stored in lower 8 bits)
//   8 = Music note (8x8, stored in lower 8 bits)
// ============================================================

module sprite_rom (
    input  wire        clk,
    input  wire [7:0]  addr,
    output reg  [15:0] data
);

    always @(posedge clk) begin
        case (addr)

            // ---- Sprite 0: Happy (standing) ----
            8'h00: data <= 16'b0000_0111_1110_0000;
